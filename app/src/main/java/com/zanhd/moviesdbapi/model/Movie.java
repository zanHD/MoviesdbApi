package com.zanhd.moviesdbapi.model;

public class Movie {

    private static Movie instance = null;
    private boolean adult;
    private int id;
    private String langauge;
    private String title;
    private String overview;
    private Double popularity;
    private String posterPath;
    private String releaseDate;
    private int voteAverage;

    private String PosterPrefix = "https://image.tmdb.org/t/p/original";
 //-----------------------------------------------------------------------------
    public static void setInstance(Movie movie) {
        instance = movie;
    }
    public static Movie getInstance() {
        return instance;
    }
//-------------------------------------------------------------------------------
    public Movie() {}

    public boolean isAdult() {
        return adult;
    }

    public void setAdult(boolean adult) {
        this.adult = adult;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getLangauge() {
        return langauge;
    }

    public void setLangauge(String langauge) {
        this.langauge = langauge;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public String getOverview() {
        return overview;
    }

    public void setOverview(String overview) {
        this.overview = overview;
    }

    public Double getPopularity() {
        return popularity;
    }

    public void setPopularity(Double popularity) {
        this.popularity = popularity;
    }

    public String getPosterPath() {
        return PosterPrefix + posterPath;
    }

    public void setPosterPath(String posterPath) {
        this.posterPath = posterPath;
    }

    public String getReleaseDate() {
        return releaseDate;
    }

    public void setReleaseDate(String releaseDate) {
        this.releaseDate = releaseDate;
    }

    public int getVoteAverage() {
        return voteAverage;
    }

    public void setVoteAverage(int voteAverage) {
        this.voteAverage = voteAverage;
    }

    public int getVoteCount() {
        return voteCount;
    }

    public void setVoteCount(int voteCount) {
        this.voteCount = voteCount;
    }

    int voteCount;


}
